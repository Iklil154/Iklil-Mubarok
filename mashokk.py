#variabel dan tipe data 

x = 15
y = 10
surya = x + y
print("hasil penjumlahan",surya)
suki = x - y
print("hasil pengurangan",suki)
apaan = x * y
print("hasil perkalian",apaan)
wadu = x ** y
print("hasil pangkat",wadu)
woke = x / y
print("hasil pembagian",woke)
siti = x % y
print("hasil modulus",siti)

